#[inline]
pub fn sandwich_rotor_rotor<T: Float>(v: &Rotor<T>, x: &Rotor<T>) -> Rotor<T> {
    Rotor::new(
        -(v.xy() * x.xy() * v.s())
            + v.s() * x.s() * v.s()
            + v.xy() * x.s() * v.xy()
            + v.s() * x.xy() * v.xy(),
        v.s() * x.xy() * v.s() - v.s() * x.s() * v.xy()
            + v.xy() * x.s() * v.s()
            + v.xy() * x.xy() * v.xy(),
    )
}
